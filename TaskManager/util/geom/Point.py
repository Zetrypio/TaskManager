# -*- coding:utf-8 -*-

class Point:
    """
    Classe contenant un X et un Y.
    """
    def __init__(self, x=0, y=0):
        """
        Constructeur du point.
        @param x: la position X du point.
        @param y: la position Y du point.
        """
        self.x = x
        self.y = y
